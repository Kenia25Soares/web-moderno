// Closure e o fechamento algo envolvendo uma função e seu escopo léxico, permitindo que a função acesse variáveis externas mesmo após a execução da função pai ter terminado.

// Closure é o escopo criado quando uma função é declarada
// Esse escopo permite a função acessar e manipular variáveis externas à função

// Contexto léxico em ação!

const x = 'Global'

function fora() {
    const x = 'Local'
    function dentro() {
        return x
    }
    return dentro
}

const minhaFuncao = fora()
console.log(minhaFuncao())