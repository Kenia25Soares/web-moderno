// 25) Escrever um programa para exibir os números de 1 até 50 na tela.

function contador () {
    for (let i = 1; i<=50; i++) {
        console.log(i);
    }
}
contador() //vai sendo incrementado o valor de i, a cada repetição do laço, até chegar a 50.
//  O resultado será a impressão dos números de 1 a 50.