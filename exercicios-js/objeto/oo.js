 // Principios importantes:
 // 1. abstracao
 // 2. encapsulamento
 // 3. herança (prototype)
 // 4. polimorfismo

 // Abstração é o processo de reduzir a complexidade de um sistema, focando apenas nos aspectos essenciais e ignorando os detalhes irrelevantes.
 // Em programação orientada a objetos, a abstração é alcançada através da criação de classes e objetos que representam entidades do mundo real,
 //  permitindo que os programadores se concentrem nas funcionalidades principais sem se preocupar com a implementação interna.

// Encapsulamento é o princípio de ocultar os detalhes internos de um objeto e fornecer uma interface pública para interagir com ele.
//  Isso ajuda a proteger os dados e a garantir que o objeto seja usado de maneira consistente, evitando que o estado interno seja modificado
// diretamente por código externo.

// Herança é um mecanismo que permite que uma classe (chamada de classe filha ou subclasse) herde propriedades e comportamentos de outra classe
//  (chamada de classe pai ou superclasse).
// Isso promove a reutilização de código e a criação de hierarquias de classes, onde as subclasses podem estender ou modificar o comportamento da
//  superclasse.

// Polimorfismo é a capacidade de um objeto assumir muitas formas. Em programação orientada a objetos, isso significa que objetos de diferentes classes
//  podem ser tratados como objetos de uma classe comum, geralmente através de uma interface ou classe base.
// Isso permite que o mesmo código funcione com objetos de diferentes tipos, promovendo a flexibilidade e a extensibilidade do sistema.

// Priorize composition over inheritance (priorize a composição em vez da herança) é um princípio de design que sugere que, em vez de criar hierarquias
// complexas de classes através da herança, os desenvolvedores devem preferir compor objetos usando outras classes ou interfaces.
// Isso promove a flexibilidade e a reutilização de código, permitindo que os objetos sejam construídos a partir de componentes menores e mais simples,
//  em vez de depender de uma estrutura rígida de herança.
